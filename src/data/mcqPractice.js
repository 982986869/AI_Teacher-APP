// Returns { answered, total, score } for a chapter (defaults when none yet).
export function getMcqProgress(subject, chapter) {
  const subj = MCQ_DATA[subject];
  const entry = subj ? subj[chapter] : null;
  if (entry && entry.progress) {
    return {
      answered: entry.progress.answered || 0,
      total: entry.progress.total || 50,
      score: entry.progress.score || 0,
    };
  }
  return { answered: 0, total: 50, score: 0 };
}

// Returns an array of sub-topic objects { name, answered, total, score }.
export function getMcqSubtopics(subject, chapter) {
  const subj = MCQ_DATA[subject];
  const entry = subj ? subj[chapter] : null;
  return entry && Array.isArray(entry.subtopics) ? entry.subtopics : [];
}

