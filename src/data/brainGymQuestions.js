// Proper unbiased shuffle (Fisher-Yates) — every ordering equally likely.
function fisherYates(arr) {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

// Small session memory of recently served questions (by text) so consecutive
// rounds don't repeat the same items. Persists across quiz-screen remounts for
// the life of the JS session; resets on reload. ~2 rounds deep.
const RECENT_QUESTIONS = [];
const RECENT_MAX = 12;

// Filter by skill (+ level) and return `count` random questions as { q, answer }.
// Gracefully widens the pool if a skill+level bucket is too small.
export function pickQuestions({ skill, level, count = 5 }) {
  const has = (arr) => arr.length >= count;

  let pool = BRAIN_GYM_QUESTIONS.filter((x) => x.skill === skill && x.level === level);
  if (!has(pool)) pool = BRAIN_GYM_QUESTIONS.filter((x) => x.skill === skill);
  if (!has(pool)) pool = BRAIN_GYM_QUESTIONS.filter((x) => x.level === level);
  if (!has(pool)) pool = [...BRAIN_GYM_QUESTIONS];

  // Prefer questions not seen in recent rounds; fall back to the full pool
  // only if avoiding repeats would leave too few to fill the round.
  const recent = new Set(RECENT_QUESTIONS);
  const fresh = pool.filter((x) => !recent.has(x.q));
  const usable = has(fresh) ? fresh : pool;

  const picked = fisherYates(usable).slice(0, count);

  // Remember what we served so the next round avoids these.
  picked.forEach((x) => RECENT_QUESTIONS.push(x.q));
  while (RECENT_QUESTIONS.length > RECENT_MAX) RECENT_QUESTIONS.shift();

  return picked.map((x) => ({ q: x.q, answer: x.answer }));
}
